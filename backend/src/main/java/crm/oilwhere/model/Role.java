package crm.oilwhere.model;

/**
 * Enum representing the different roles a user can have within the system.
 * Roles include MARKETING, SALES, and ADMIN.
 */
public enum Role {
    /**
     * Represents a user in the marketing department.
     */
    MARKETING,

    /**
     * Represents a user in the sales department.
     */
    SALES,

    /**
     * Represents an administrative user with higher-level permissions.
     */
    ADMIN
}
